import turtle

bob=turtle.Turtle()

bob.forward(100)    #movers the cursor 100 pixels straight forward

bob.left(45)       #moves the cursor towards the left direction with an angel of 45 degrees.

bob.forward(100)

bob.right(90)

bob.forward(100)

bob.forward(100)

turtle.done()           #This is the ending of the turtle code and opens the gui